public class Point{
	public double abscisse, ordonnee;
	public Point(double a, double b){
		this.abscisse=a;
		this.ordonnee=b;
	}
	public Point() {
		this.abscisse=0;
		this.ordonnee=0;
	}
	public double abscisse(){
		return this.abscisse;
	}
	public double ordonnee(){
		return this.ordonnee;
	}

}